package com.model;

public class Adm {
	private String adm_id;
	private String adm_passwd;
	private String adm_name;
	private String adm_phone;
	private String adm_addr;
	public String getAdm_id() {
		return adm_id;
	}
	public void setAdm_id(String adm_id) {
		this.adm_id = adm_id;
	}
	public String getAdm_passwd() {
		return adm_passwd;
	}
	public void setAdm_passwd(String adm_passwd) {
		this.adm_passwd = adm_passwd;
	}
	public String getAdm_name() {
		return adm_name;
	}
	public void setAdm_name(String adm_name) {
		this.adm_name = adm_name;
	}
	public String getAdm_phone() {
		return adm_phone;
	}
	public void setAdm_phone(String adm_phone) {
		this.adm_phone = adm_phone;
	}
	public String getAdm_addr() {
		return adm_addr;
	}
	public void setAdm_addr(String adm_addr) {
		this.adm_addr = adm_addr;
	}
	

}
